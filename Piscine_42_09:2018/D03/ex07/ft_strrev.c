char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	swap;

	j = 0;
	i = 0;
	while (str[i])
	{
		i++;
	}
	i--;
	while (i > j)
	{
		swap = str[j];
		str[j] = str[i];
		str[i] = swap;
		j++;
		i--;
	}
	return (str);
}
