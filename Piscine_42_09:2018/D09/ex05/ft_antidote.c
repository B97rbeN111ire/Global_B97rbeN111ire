int		ft_antidote(int i, int j, int k)
{
	int		temp;

	temp = 0;
	if ((i <= k && i >= j) || (i >= k && i <= j))
		return (i);
	if ((j >= i && j <= k) || (i <= j && j >= k))
		return (j);
	if ((k >= i && k <= j) || (k <= i && k >= j))
		return (k);
	else
		return (0);
}
